#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		j;
	int		n;

	i = 0;
	j = 1;
	n = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\t' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - '0');
		i++;
		j++;
	}
	if (str[i - j] == '-')
		n = -n;
	return (n);
}
